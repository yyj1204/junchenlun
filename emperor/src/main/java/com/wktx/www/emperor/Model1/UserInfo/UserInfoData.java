package com.wktx.www.emperor.Model1.UserInfo;

public class UserInfoData {
    private String msg;
    private int code;
    private UserInfoDataInfo info;

    public String getMsg() {
        return this.msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return this.code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public UserInfoDataInfo getInfo() {
        return this.info;
    }

    public void setInfo(UserInfoDataInfo info) {
        this.info = info;
    }
}
